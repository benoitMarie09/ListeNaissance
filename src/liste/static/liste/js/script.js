function openChoisir() {
  overlay = document.getElementById("overlay");
  overlay.classList.remove("hidden");
  choisir = document.getElementById("help__choisir");
  choisir.classList.remove("hidden");
}
function closeChoisir() {
  overlay = document.getElementById("overlay");
  overlay.classList.add("hidden");
  choisir = document.getElementById("help__choisir");
  choisir.classList.add("hidden");
}
